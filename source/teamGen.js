const HTMLGen = function combineSects(data) {
    employeeArray= []
    for (let i=0; i<data.length; i++){
        const employee = data[i]
        const role= employee.checkRole();
        if (role === 'Manager'){
            const managerSect = managersSect(employee);
            employeeArray.push(managerSect);
        }
        if (role === 'Intern') {
            const internSect = internsSect(employee);
            employeeArray.push(internSect);
        }
        if (role === 'Engineer'){
            const engineerSect = engineersSect(employee);
            employeeArray.push(engineerSect);
        }
    }
    const employeeInfo = employeeArray.join('')
    const generateTeam = teamPage(employeeInfo);
    return generateTeam;
};

